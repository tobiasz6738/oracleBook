public class DivisionXByTwo
{
    private double x ;
    private double y ;


    public double divisionForTwo()
    {
        return y = x / 2;
    }

    public void setX(int x)
    {
        this.x = x;
    }




    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }

    public void setY(double y)
    {
        this.y = y;
    }
}
