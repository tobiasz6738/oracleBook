package tobiasz.nocneNauki.dzien2;

public class Petent {

    private double waga;
    private double wzrost;

    public Petent(double waga, double wzrost)
    {
        this.waga = waga;

        this.wzrost = wzrost;
    }

    public double getWaga()
    {
        return waga;
    }

    public double getWzrost()
    {
        return wzrost;
    }
}
